#include<iostream>
using namespace std;
int main()
{
	int i;
	int j;
	int k;
	int l;
	int m;
	int n;
	for(i=0;i<1;i++)
	{
		for(j=0;j<=i;j++)
		{
			cout<<"  "<<"   "<<"*";
		}
		cout<<"\n";
		
	}
	for(k=0;k<2;k++)
	{
		for(l=0;l<=k;l++)
		{
			cout<<"  "<<"*";
		}
	}
	cout<<"\n";
	for(m=0;m<1;m++)
	{
		for(n=0;n<5;n++)
		{
			cout<<" "<<"*";
		}
	}
}
