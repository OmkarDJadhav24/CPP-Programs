#include<iostream>
using namespace std;
int main()
{
	int a=2;
	switch(a)
	{
		case 1:cout<<"\n The number is 1";
		break;
		
		case 2:cout<<"\n The number is 2";
		break;
		
		case 10:cout<<"\n The number is 10";
		break;
		
		case 13:cout<<"\n The number is 13";
		break;
		
		default:cout<<"\n The number is not found";
		break;
	}
	return 0;
}
